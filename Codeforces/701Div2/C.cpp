int a, b;

inline void work() {
    cin >> a >> b;
    LL ans = 0;
    for (int k = 1; k * (k + 2) <= a; ++k) {
        int bmax = min(a / k - 1, b);
        int bmin = k + 1;
        if (bmin <= bmax) {
            ans += bmax - bmin + 1;
        }
    }
    cout << ans << "\n";
}

int main() {
    // freopen("input.txt", "r", stdin);
    quickread();
    int T;
    cin >> T;
    while (T--) {
        work();
    }
    return 0;
}